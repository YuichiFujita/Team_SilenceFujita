#ifndef _FLASH_H_				// このマクロ定義がされていない場合
#define _FLASH_H_				// 二重インクルード防止のマクロを定義する

//**********************************************************************************************************************
//	インクルードファイル
//**********************************************************************************************************************
#include "main.h"

//**********************************************************************************************************************
//	構造体定義(Flash)
//**********************************************************************************************************************
typedef struct
{
	D3DXCOLOR col;			// 位置
	float fDestAlpha;		// 目標の透明度
	float fDiffAlpha;		// 透明度の差分
	bool bUse;				// 使用状況
}Flash;

//**********************************************************************************************************************
//	プロトタイプ宣言
//**********************************************************************************************************************
void InitFlash(void);			// フェードの初期化処理
void UninitFlash(void);			// フェードの終了処理
void UpdateFlash(void);			// フェードの更新処理
void DrawFlash(void);			// フェードの描画処理
void SetFlash(void);			// フェードの設定処理
Flash GetFlash(void);			// フェードの取得処理

#endif